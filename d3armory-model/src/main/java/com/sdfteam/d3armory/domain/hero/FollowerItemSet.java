package com.sdfteam.d3armory.domain.hero;

import com.sdfteam.d3armory.domain.artisanblacksmith.Item;

public class FollowerItemSet {
	private Item leftFinger;
	private Item mainHand;
	private Item neck;
	private Item offHand;
	private Item rightFinger;

	public Item getLeftFinger() {
		return leftFinger;
	}

	public void setLeftFinger(Item leftFinger) {
		this.leftFinger = leftFinger;
	}

	public Item getMainHand() {
		return mainHand;
	}

	public void setMainHand(Item mainHand) {
		this.mainHand = mainHand;
	}

	public Item getNeck() {
		return neck;
	}

	public void setNeck(Item neck) {
		this.neck = neck;
	}

	public Item getOffHand() {
		return offHand;
	}

	public void setOffHand(Item offHand) {
		this.offHand = offHand;
	}

	public Item getRightFinger() {
		return rightFinger;
	}

	public void setRightFinger(Item rightFinger) {
		this.rightFinger = rightFinger;
	}

	@Override
	public String toString() {
		return "FollowerItemSet [leftFinger=" + leftFinger + ", mainHand="
				+ mainHand + ", neck=" + neck + ", offHand=" + offHand
				+ ", rightFinger=" + rightFinger + "]";
	}

}
